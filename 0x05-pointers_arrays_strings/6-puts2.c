/**
 * puts2 - a function that prints every other character of a string
 *
 * @str: take a string
 *
 * Return: void
*/

void puts2(char *str)
{
	int i;
	char ch;

	str[0] = "ch";
	i = 0;
	while (i >= 0)
	i++;
	{
	printf("%s\n", str);
	if (str[i] == ch)
	printf("%s\n", str);
	}
}
